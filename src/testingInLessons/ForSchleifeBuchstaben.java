package testingInLessons;

public class ForSchleifeBuchstaben {
	public static void main(String[] args) {
	System.out.print("Alphabet: ");
	for (char buchstabe = 'A'; buchstabe <= 'Z'; buchstabe++) {
	    System.out.print(buchstabe);
	}
	System.out.println();
	
	System.out.print("Zahlen 1-9: ");
		for (char c = '0'; c <= '9'; c++) {
			System.out.print(c);
		}
	
	}	
}
